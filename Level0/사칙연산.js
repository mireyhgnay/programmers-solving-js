// 두 수의 합1
function solution(num1, num2) {
    const answer = num1 + num2;
    return answer;
}
// 두 수의 합2 (화살표 함수)
const solution = (num1, num2) => num1 + num2;

// 두 수의 차
const solution = (num1, num2) => num1 - num2;

// 두 수의 곱
const solution = (num1, num2) => num1 * num2;

// 몫 구하기
const solution = (num1, num2) => parseInt(num1 / num2);

// 나머지 구하기
const solution = (num1, num2) => num1 % num2;